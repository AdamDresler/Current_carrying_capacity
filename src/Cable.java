public class Cable {
    private String type;
    private int numberOfWires;
    private String insulation;
    private String wiresMaterial;
    private double crossSection;


    public String getWiresMaterial() {
        return wiresMaterial;
    }

    public void setWiresMaterial(String wiresMaterial) {
        this.wiresMaterial = wiresMaterial;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getNumberOfWires() {
        return numberOfWires;
    }

    public void setNumberOfWires(int numberOfWires) {
        this.numberOfWires = numberOfWires;
    }

    public String getInsulation() {
        return insulation;
    }

    public void setInsulation(String insulation) {
        this.insulation = insulation;
    }

    public double getCrossSection() {
        return crossSection;
    }

    public void setCrossSection(double crossSection) {
        this.crossSection = crossSection;
    }





}
